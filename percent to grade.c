//WAP to find grade of student by his percentage
#include<stdio.h>
int main(){
	int perc;
	printf("enter your percent ");
	scanf("%d",&perc);
	
	if(100>=perc && perc>=90){
		printf("A++");
	}
	else if(90>perc && perc>=80){
		printf("A");
	}
	else if(80>perc && perc>=70){
		printf("B");
	}
	else if(70>perc && perc>=60){
		printf("C");
	}
	else if(60>perc && perc>=50){
		printf("D");
	}	
	else if(50>perc && perc>=0){
		printf("NEED IMPROVEMENT");
	}
	return 0;
}
