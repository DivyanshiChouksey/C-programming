//WAP to find min value in an array(user input)

#include<stdio.h>
int main()
{	int n,i;
	int arr[50] = {};
	printf("enter the size of array you want\n");
	scanf("%d",&n);
		for(i=0; i<n; i++)
		scanf("%d",&arr[i]);
		
	int min = arr[0];
		for(int i=1; i<5; i++)
		if(min > arr[i]){
			min = arr[i];
		}
		printf("Minimum value of given array is %d",min);
	return 0;
}
